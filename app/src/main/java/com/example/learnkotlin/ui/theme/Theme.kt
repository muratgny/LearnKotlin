@Composable
fun LearnKotlinTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),//Defining parameter for dark theme
    dynamicColor: Boolean = true,//Defining parameter for dynamic color. Material 3 strongly focuses on user personalization
    // - a new feature in Material 3 is Dynamic Color which creates a theme for your app based on the user's wallpaper.
    // This way if the user loves green and has a blue phone background, their Woof app will also be blue to reflect that.
    // Dynamic theming is only on certain devices that are running Android 12 and up.
    content: @Composable() () -> Unit
) {
    val colors = if (!useDarkTheme) {
        LightColors
    } else {
        DarkColors
    }

    MaterialTheme(
        colorScheme = colors,
        typography = Typography,
        shapes = Shapes,
        content = content
    )
}